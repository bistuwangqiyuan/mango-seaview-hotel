function t(key) {
  const lang = document.documentElement.lang === 'ru' ? 'ru' : document.documentElement.lang === 'en' ? 'en' : 'zh';
  return I18N[lang]?.[key] ?? I18N.zh[key] ?? key;
}

function getCurrentLang() {
  const html = document.documentElement.lang;
  if (html === 'ru') return 'ru';
  if (html === 'en') return 'en';
  return 'zh';
}
